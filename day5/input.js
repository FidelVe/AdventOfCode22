function makeMove(amountToMove, from, to, state) {
  const copyOfState = getCopyOfState(state);

  for (let i = 0; i < amountToMove; i++) {
    copyOfState[to].push(copyOfState[from].pop());
  }

  return copyOfState;
}

function makeMove2(amountToMove, from, to, state) {
  let copyOfState = getCopyOfState(state);
  const setRemoved = copyOfState[from].slice(
    copyOfState[from].length - amountToMove
  );
  const setLeft = copyOfState[from].slice(
    0,
    copyOfState[from].length - amountToMove
  );

  copyOfState[from] = [...setLeft];
  copyOfState[to] = [...copyOfState[to], ...setRemoved];
  return copyOfState;
}

function getTopOfEach(state) {
  let allTops = "";

  for (let i = 1; i < 10; i++) {
    const key = i.toString();
    const top =
      state[key][state[key].length - 1] == null
        ? "-"
        : state[key][state[key].length - 1];
    allTops = allTops + top;
  }

  return allTops;
}

